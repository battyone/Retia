namespace Retia.Helpers
{
    /// <summary>
    /// A structure that contains minimum and maximum value.
    /// </summary>
    /// <typeparam name="T"></typeparam>
    public struct MinMax<T>
    {
        /// <summary>
        /// Minimum value.
        /// </summary>
        public T Min;

        /// <summary>
        /// Maximum value.
        /// </summary>
        public T Max;
    }
}